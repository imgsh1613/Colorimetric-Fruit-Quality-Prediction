import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';
import { getFruitInfo } from '../data/fruitKnowledge';

export const generateFruitReport = (results) => {
  const info = getFruitInfo(results.fruit_type);
  const isFresh = results.quality_status === 'Fresh';
  const doc = new jsPDF('p', 'mm', 'a4');
  const pageWidth = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();
  const margin = 20;
  const contentWidth = pageWidth - margin * 2;
  let y = 20;

  const addPage = () => {
    doc.addPage();
    y = 20;
  };

  const checkPage = (needed = 30) => {
    if (y + needed > 270) addPage();
  };

  const drawAutoTable = (options) => {
    autoTable(doc, options);
    y = doc.lastAutoTable.finalY + 12;
  };

  const drawSectionTitle = (num, title) => {
    checkPage(40);
    doc.setFontSize(16);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(30, 30, 30);
    doc.text(`${num}. ${title}`, margin, y);
    y += 10;
  };

  const drawSubTitle = (title) => {
    checkPage(15);
    doc.setFontSize(11);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(50, 50, 50);
    doc.text(title, margin, y);
    y += 6;
  };

  const drawBulletList = (items) => {
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9);
    doc.setTextColor(60, 60, 60);
    items.forEach((item) => {
      checkPage(7);
      // Remove emojis for PDF compatibility
      const cleanText = item.replace(/[\u{1F300}-\u{1FAFF}\u{2600}-\u{27BF}\u{FE00}-\u{FE0F}\u{1F900}-\u{1F9FF}]/gu, '').trim();
      const safeText = cleanText.replace(/[^\x20-\x7E\xA0-\xFF]/g, '').trim();
      if (safeText) {
        doc.text(`- ${safeText}`, margin + 2, y);
        y += 5;
      }
    });
    y += 4;
  };

  // ============================================================
  // COVER PAGE (Dark theme)
  // ============================================================
  doc.setFillColor(15, 15, 20);
  doc.rect(0, 0, pageWidth, pageHeight, 'F');

  // Green accent bar
  doc.setFillColor(34, 197, 94);
  doc.rect(margin, 38, contentWidth, 1.5, 'F');

  // Header text
  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(120, 120, 120);
  doc.text('FruitSense AI - Quality Intelligence Report', margin, 34);

  // Fruit name
  doc.setFontSize(32);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(240, 240, 240);
  doc.text(`${results.fruit_type} Analysis`, margin, 60);

  // Status
  doc.setFontSize(14);
  doc.setTextColor(160, 160, 160);
  doc.text(`Quality Status: ${results.quality_status}`, margin, 72);

  // Quality Score
  doc.setFontSize(72);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(isFresh ? 34 : 249, isFresh ? 197 : 115, isFresh ? 94 : 22);
  doc.text(`${results.overallScore}`, pageWidth / 2, 120, { align: 'center' });

  doc.setFontSize(12);
  doc.setTextColor(100, 100, 100);
  doc.text('Overall Quality Score', pageWidth / 2, 132, { align: 'center' });

  // Metadata block
  const metaY = 155;
  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(140, 140, 140);

  const date = new Date().toLocaleDateString('en-US', {
    weekday: 'long', year: 'numeric', month: 'long', day: 'numeric',
  });
  const time = new Date().toLocaleTimeString('en-US', { hour: '2-digit', minute: '2-digit' });

  doc.text(`Date: ${date}`, margin, metaY);
  doc.text(`Time: ${time}`, margin, metaY + 7);
  doc.text(`Confidence: ${Math.round(results.confidence * 100)}%`, margin, metaY + 14);

  if (info) {
    doc.text(`Scientific Name: ${info.scientificName}`, margin, metaY + 25);
    doc.text(`Family: ${info.family}  |  Category: ${info.category}`, margin, metaY + 32);
    doc.text(`Origin: ${info.origin}`, margin, metaY + 39);
    doc.text(`Season: ${info.season}`, margin, metaY + 46);
  }

  // Cover footer
  doc.setFillColor(34, 197, 94);
  doc.rect(margin, 265, contentWidth, 1, 'F');
  doc.setFontSize(8);
  doc.setTextColor(80, 80, 80);
  doc.text('Generated by FruitSense AI  |  GAUTAM Project  |  Powered by MobileNetV2', margin, 272);

  // ============================================================
  // PAGE 2+: Content pages (White background)
  // ============================================================

  // --- 1. Quality Assessment ---
  addPage();
  doc.setFillColor(255, 255, 255);
  doc.rect(0, 0, pageWidth, pageHeight, 'F');

  drawSectionTitle(1, 'Quality Assessment');

  const qualityBody = [
      ['Overall Score', `${results.overallScore} / 100`, results.overallScore >= 80 ? 'Excellent' : results.overallScore >= 60 ? 'Good' : results.overallScore >= 40 ? 'Fair' : 'Poor'],
      ['Freshness', `${results.freshness}%`, results.freshness >= 80 ? 'Fresh' : 'Deteriorating'],
      ['Ripeness', `${results.ripeness}%`, results.ripeness >= 80 ? 'Optimal' : results.ripeness >= 50 ? 'Acceptable' : 'Under/Over-ripe'],
      ['Defect Count', `${results.defectCount}`, results.defectCount <= 1 ? 'Minimal' : results.defectCount <= 3 ? 'Moderate' : 'Significant'],
      ['Confidence', `${Math.round(results.confidence * 100)}%`, results.confidence >= 0.9 ? 'High' : 'Moderate'],
  ];

  if (results.predicted_weight_loss_percent !== undefined) {
      qualityBody.push(['Predicted Weight Loss (SE-CNN)', `${(results.predicted_weight_loss_percent * 100).toFixed(1)}%`, results.predicted_weight_loss_percent > 0.05 ? 'Elevated Loss' : 'Normal/Stable']);
      qualityBody.push(['Predicted Firmness (SE-CNN)', `${results.predicted_hardness_N?.toFixed(2)} N`, results.predicted_hardness_N > 15 ? 'Firm (Fresh)' : 'Softening']);
      qualityBody.push(['Predicted Brittleness (SE-CNN)', `${results.predicted_brittleness?.toFixed(2)}`, 'Deformation Proxy']);
  }

  if (results.colorimetric_data) {
      qualityBody.push(['Color: L* (Lightness)', `${results.colorimetric_data.L}`, 'CIE L*a*b* Analysis']);
      qualityBody.push(['Color: a* (Red-Green)', `${results.colorimetric_data.a}`, 'Chrominance Proxy']);
      qualityBody.push(['Color: b* (Yellow-Blue)', `${results.colorimetric_data.b}`, 'Chrominance Proxy']);
      qualityBody.push(['Hue Angle (H°)', `${results.colorimetric_data.H}°`, 'Angular Phase']);
  }

  drawAutoTable({
    startY: y,
    margin: { left: margin, right: margin },
    head: [['Metric', 'Value', 'Rating']],
    body: qualityBody,
    theme: 'grid',
    headStyles: { fillColor: [34, 197, 94], textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 10 },
    bodyStyles: { fontSize: 10 },
    alternateRowStyles: { fillColor: [245, 250, 245] },
    styles: { cellPadding: 4 },
  });

  // --- 2. Shelf Life & Fridge Guide ---
  if (info) {
    drawSectionTitle(2, 'Shelf Life & Storage Guide');

    drawAutoTable({
      startY: y,
      margin: { left: margin, right: margin },
      head: [['Storage Method', 'Duration', 'Notes']],
      body: [
        ['Whole in Fridge', info.fridgeGuide.wholeInFridge, `Temp: ${info.storageTemp}`],
        ['Cut / Sliced in Fridge', info.fridgeGuide.cutInFridge, `Humidity: ${info.humidity}`],
        ['Room Temperature', info.fridgeGuide.roomTemperature, info.ethyleneProducer ? 'Ethylene producer - keep away from sensitive produce' : 'Safe near other produce'],
        ['Freezer', info.fridgeGuide.freezer, 'Best for long-term storage'],
        ['Current Status', isFresh ? info.shelfLife.fresh.label : (results.overallScore >= 40 ? 'Moderate (Edible)' : info.shelfLife.rotten.label), isFresh ? `Approx. ${info.shelfLife.fresh.fridgeDays} days in fridge` : (results.overallScore >= 40 ? '1 Day (Consume Soon)' : 'Discard immediately')],
      ],
      theme: 'grid',
      headStyles: { fillColor: [59, 130, 246], textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 10 },
      bodyStyles: { fontSize: 10 },
      alternateRowStyles: { fillColor: [240, 245, 255] },
      styles: { cellPadding: 4 },
    });

    drawSubTitle('Storage Tips:');
    drawBulletList(info.storageTips);
  }

  // --- 3. Nutritional Information ---
  if (info) {
    drawSectionTitle(3, 'Nutritional Information (per 100g)');

    const nutritionRows = Object.entries(info.nutritionalInfo).map(([key, value]) => {
      const label = key.replace(/([A-Z])/g, ' $1').replace(/^./, (s) => s.toUpperCase()).trim();
      return [label, String(value)];
    });

    drawAutoTable({
      startY: y,
      margin: { left: margin, right: margin },
      head: [['Nutrient', 'Amount']],
      body: nutritionRows,
      theme: 'grid',
      headStyles: { fillColor: [239, 68, 68], textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 10 },
      bodyStyles: { fontSize: 10 },
      alternateRowStyles: { fillColor: [255, 248, 248] },
      styles: { cellPadding: 4 },
      columnStyles: { 0: { fontStyle: 'bold' } },
    });

    drawSubTitle('Health Benefits:');
    drawBulletList(info.healthBenefits);
  }

  // --- 4. Culinary Guide ---
  if (info) {
    drawSectionTitle(4, "Chef's Culinary Guide");

    // Flavor profile
    doc.setFontSize(10);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(50, 50, 50);
    doc.text('Flavor Profile:', margin, y);
    doc.setFont('helvetica', 'normal');
    doc.text(info.flavorProfile, margin + 28, y, { maxWidth: contentWidth - 28 });
    y += 10;

    // Varieties
    checkPage(15);
    doc.setFont('helvetica', 'bold');
    doc.text('Popular Varieties:', margin, y);
    doc.setFont('helvetica', 'normal');
    doc.text(info.varieties.join(', '), margin + 35, y, { maxWidth: contentWidth - 35 });
    y += 10;

    // Culinary uses
    drawSubTitle('Culinary Uses:');
    drawBulletList(info.culinaryUses);

    // Recipes table
    checkPage(50);
    drawAutoTable({
      startY: y,
      margin: { left: margin, right: margin },
      head: [['Recipe', 'Difficulty', 'Time']],
      body: info.recipes.map((r) => [r.name, r.difficulty, r.time]),
      theme: 'grid',
      headStyles: { fillColor: [168, 85, 247], textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 10 },
      bodyStyles: { fontSize: 10 },
      alternateRowStyles: { fillColor: [250, 245, 255] },
      styles: { cellPadding: 4 },
    });

    // Pairings
    drawSubTitle('Best Pairings:');
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9);
    doc.setTextColor(60, 60, 60);
    doc.text(`Cheese: ${info.pairings.cheese.join(', ')}`, margin + 2, y);
    y += 5;
    doc.text(`Spices: ${info.pairings.spices.join(', ')}`, margin + 2, y);
    y += 5;
    doc.text(`Other: ${info.pairings.other.join(', ')}`, margin + 2, y);
    y += 10;
  }

  // --- 5. Quality Indicators ---
  if (info) {
    drawSectionTitle(5, 'Quality Indicators');

    drawSubTitle(isFresh ? 'Fresh Quality Signs (Confirmed):' : 'Spoilage Signs (Detected):');
    const indicators = isFresh ? info.freshIndicators : info.rottenIndicators;
    drawBulletList(indicators);

    drawSubTitle('Known Defect Types:');
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(9);
    doc.setTextColor(60, 60, 60);
    doc.text(info.commonDefects.join('  |  '), margin + 2, y, { maxWidth: contentWidth });
    y += 10;
  }

  // --- 6. Buying Guide ---
  if (info) {
    drawSectionTitle(6, 'Buying Guide');
    drawBulletList(info.buyingTips);
  }

  // --- 7. Compression / Tactile Data ---
  if (info) {
    drawSectionTitle(7, 'Mechanical Properties (Compression Test)');

    const forceDiff = Math.round(((info.avgCompressionForce.fresh - info.avgCompressionForce.rotten) / info.avgCompressionForce.fresh) * 100);

    drawAutoTable({
      startY: y,
      margin: { left: margin, right: margin },
      head: [['Property', 'Fresh', 'Rotten']],
      body: [
        ['Avg Compression Force', `${info.avgCompressionForce.fresh} kN`, `${info.avgCompressionForce.rotten} kN`],
        ['Peak Force Range', info.peakForceRange.fresh, info.peakForceRange.rotten],
        ['Force Difference', `${forceDiff}% stronger when fresh`, '-'],
      ],
      theme: 'grid',
      headStyles: { fillColor: [6, 182, 212], textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 10 },
      bodyStyles: { fontSize: 10 },
      alternateRowStyles: { fillColor: [240, 252, 255] },
      styles: { cellPadding: 4 },
    });
  }

  // --- 8. Dataset & Model Info ---
  if (info) {
    drawSectionTitle(8, 'Dataset & AI Model Information');

    drawAutoTable({
      startY: y,
      margin: { left: margin, right: margin },
      head: [['Parameter', 'Value']],
      body: [
        ['Model Architecture', 'Squeeze-and-Excitation CNN (SE-CNN) with Regression'],
        ['Regression Target Metrics', 'Weight Loss (exp decay proxy), Firmness (N), Brittleness'],
        ['Cross-Validation Strategy', 'Stratified 5-Fold Cross Validation'],
        ['Validation Targets', 'R² > 0.80 (Weight Loss), R² > 0.97 (Texture)'],
        ['Colorimetric Extraction', 'CIE L*a*b* Features (Hue Angle, Chroma)'],
        [`${results.fruit_type} Fresh Images`, info.datasetStats.freshImages.toLocaleString()],
        [`${results.fruit_type} Rotten Images`, info.datasetStats.rottenImages.toLocaleString()],
        [`${results.fruit_type} Total Images`, info.datasetStats.totalImages.toLocaleString()],
        ['Tactile Samples', `${info.datasetStats.tactileSamples}`],
        ['Total Dataset Size', '18,000+ images'],
      ],
      theme: 'grid',
      headStyles: { fillColor: [99, 102, 241], textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 10 },
      bodyStyles: { fontSize: 10 },
      alternateRowStyles: { fillColor: [238, 242, 255] },
      styles: { cellPadding: 4 },
      columnStyles: { 0: { fontStyle: 'bold' } },
    });
  }

  // --- 9. Fun Facts ---
  if (info && info.funFacts) {
    drawSectionTitle(9, `Fun Facts About ${results.fruit_type}`);
    drawBulletList(info.funFacts);
  }

  // --- 10. AI Recommendations ---
  if (results.recommendations && results.recommendations.length > 0) {
    drawSectionTitle(10, 'AI Recommendations');

    const recRows = results.recommendations.map((rec) => {
      const cleanTitle = rec.title.replace(/[^\x20-\x7E\xA0-\xFF]/g, '').trim();
      return [cleanTitle, rec.description];
    });

    drawAutoTable({
      startY: y,
      margin: { left: margin, right: margin },
      head: [['Recommendation', 'Details']],
      body: recRows,
      theme: 'grid',
      headStyles: { fillColor: [34, 197, 94], textColor: [255, 255, 255], fontStyle: 'bold', fontSize: 10 },
      bodyStyles: { fontSize: 9 },
      alternateRowStyles: { fillColor: [245, 250, 245] },
      styles: { cellPadding: 4 },
      columnStyles: { 0: { fontStyle: 'bold', cellWidth: 50 }, 1: { cellWidth: 'auto' } },
    });
  }

  // ============================================================
  // Page numbers on every page
  // ============================================================
  const totalPages = doc.internal.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(150, 150, 150);
    doc.text(
      `FruitSense AI Report  |  ${results.fruit_type}  |  Page ${i} of ${totalPages}`,
      pageWidth / 2, 287, { align: 'center' }
    );
  }

  // Save
  const filename = `FruitSense_${results.fruit_type}_${results.quality_status}_Report_${new Date().toISOString().split('T')[0]}.pdf`;
  doc.save(filename);
  return filename;
};
